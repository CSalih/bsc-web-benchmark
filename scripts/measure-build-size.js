const fs = require('node:fs');
const path = require('path');

function humanizeBytes(bytes, decimals = 2) {
  if (!+bytes) {
    return '0 Bytes'
  }

  const k = 1024
  const dm = decimals < 0 ? 0 : decimals
  const sizes = ['Bytes', 'KiB', 'MiB', 'GiB', 'TiB', 'PiB', 'EiB', 'ZiB', 'YiB']

  const i = Math.floor(Math.log(bytes) / Math.log(k))

  return `${parseFloat((bytes / Math.pow(k, i)).toFixed(dm))} ${sizes[i]}`
}


const directorySizeRecursive = (dir, totalSize) => {
  const files = fs.readdirSync(dir, {
    withFileTypes: true
  });

  return files
    .map(file => {
      const filePath = path.join(dir, file.name);

      if (file.isDirectory()) {
        return directorySizeRecursive(filePath);
      }

      if (
        file.name.endsWith(".ico")                // ignore icons
        || file.name === "3rdpartylicenses.txt"   // ignore licenses generated by Angular
      ) {
        return 0;
      }

      if (file.isFile()) {
        const {size} = fs.statSync(filePath);
        return size;
      }

      return 0;
    })
    .reduce((acc, size) => acc + size, 0);

}


const buildDirectorySizes = (directory) => {
  const files = fs.readdirSync(directory);
  return files
    .filter(filePath => filePath.startsWith('app-'))
    .map(filePath => path.join(directory, `${filePath}/dist`))
    .map((filePath) => {
      const size = fs.existsSync(filePath)
        ? humanizeBytes(directorySizeRecursive(filePath, 0))
        : "Not build yet"
      return {
        size: size,
        filePath
      };
    });
}


